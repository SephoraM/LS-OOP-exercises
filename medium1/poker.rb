class Card
  attr_reader :rank, :suit
  include Comparable

  VALUES = { 'Jack' => 11, 'Queen' => 12, 'King' => 13, 'Ace' => 14 }

  def initialize(rank, suit)
    @rank = rank
    @suit = suit
  end

  def to_s
    "#{rank} of #{suit}"
  end

  def value
    VALUES.fetch(@rank, @rank)
  end

  def <=>(other_card)
    value <=> other_card.value
  end
end

class Deck
  RANKS = (2..10).to_a + %w(Jack Queen King Ace).freeze
  SUITS = %w(Hearts Clubs Diamonds Spades).freeze

  def initialize
    populate
  end

  def draw
    pop ||
    repopulate_and_pop
  end

  private

  def populate
    @cards = generate_deck.shuffle
  end

  def pop
    @cards.pop
  end

  def repopulate_and_pop
    populate
    pop
  end

  def generate_deck
    SUITS.map { |suit| RANKS.map { |rank| Card.new(rank, suit) } }.flatten
  end
end

class PokerHand
  def initialize(deck)
    @hand = []
    5.times { @hand << deck.draw }
    @sorted_hand = @hand.sort
    @all_rankings = num_of_each
  end

  def print
    @hand.each { |card| puts card }
  end

  def evaluate
    case
    when royal_flush?     then 'Royal flush'
    when straight_flush?  then 'Straight flush'
    when four_of_a_kind?  then 'Four of a kind'
    when full_house?      then 'Full house'
    when flush?           then 'Flush'
    when straight?        then 'Straight'
    when three_of_a_kind? then 'Three of a kind'
    when two_pair?        then 'Two pair'
    when pair?            then 'Pair'
    else                       'High card'
    end
  end

  private
  attr_reader :sorted_hand, :all_rankings

  def num_of_each
    sorted_hand.map { |card| sorted_hand.count(card) }
  end

  def royal_flush?
    straight_flush? && sorted_hand[0].value == 10
  end

  def straight_flush?
    flush? && straight?
  end

  def four_of_a_kind?
    all_rankings.count(4) == 4
  end

  def full_house?
    three_of_a_kind? && pair?
  end

  def flush?
    suit_test = sorted_hand[0].suit
    sorted_hand.all? { |card| card.suit == suit_test }
  end

  def straight?
    tester = sorted_hand[0].value
    5.times do |idx|
      return false if tester != sorted_hand[idx].value
      tester += 1
    end
    true
  end

  def three_of_a_kind?
    all_rankings.count(3) == 3
  end

  def two_pair?
    all_rankings.count(2) == 4
  end

  def pair?
    all_rankings.count(2) == 2
  end
end

hand = PokerHand.new(Deck.new)
